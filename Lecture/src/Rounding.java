public class Rounding {
    public static void main(String[] args) {
        System.out.println("ceil(2.1) is " +Math.ceil(2.1));
        System.out.println("ceil(2.0) is " +Math.ceil(2.0));
        System.out.println("ceil(-2.0) is " +Math.ceil(-2.0));
        System.out.println("ceil(-2.1) is " +Math.ceil(-2.1));

        System.out.println("floor(2.1) is " + Math.floor(2.1)) ;
        System.out.println("floor(2.0) is " + Math.floor(2.0)) ;
        System.out.println("floor(-2.1) is " + Math.floor(-2.1));
    }
}
